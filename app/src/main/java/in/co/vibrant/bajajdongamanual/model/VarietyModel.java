package in.co.vibrant.bajajdongamanual.model;

public class VarietyModel {



    protected String id;
    protected String varietyCode;
    protected String varietyName;
    protected String varietyCategoryCode;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVarietyCode() {
        return varietyCode;
    }

    public void setVarietyCode(String varietyCode) {
        this.varietyCode = varietyCode;
    }

    public String getVarietyName() {
        return varietyName;
    }

    public void setVarietyName(String varietyName) {
        this.varietyName = varietyName;
    }

    public String getVarietyCategoryCode() {
        return varietyCategoryCode;
    }

    public void setVarietyCategoryCode(String varietyCategoryCode) {
        this.varietyCategoryCode = varietyCategoryCode;
    }
}
